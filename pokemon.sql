DROP TABLE pokemon;
DROP TABLE learns;
DROP TABLE move;
DROP TABLE ability;

CREATE TABLE pokemon (
	id integer,
	name varchar(20),
	bst integer NOT NULL,
	hp integer NOT NULL,
	atk integer NOT NULL,
	def integer NOT NULL,
	spatk integer NOT NULL,
	spdef integer NOT NULL,
	speed integer NOT NULL,
	type1 varchar(12) NOT NULL,
	type2 varchar(12),
	ability1 integer,
	ability2 integer,
	hidden_ability integer,
	PRIMARY KEY (name, id), 
	FOREIGN KEY (ability1) REFERENCES ability(id),
	FOREIGN KEY (ability2) REFERENCES ability(id),
	FOREIGN KEY (hidden_ability) REFERENCES ability(id)
);

CREATE TABLE learns (
	pokemon integer,
	move integer,
	method text NOT NULL,
	PRIMARY KEY (pokemon, move),
	FOREIGN KEY (pokemon) references pokemon(id),
	FOREIGN KEY (move) references move(id)
);

CREATE TABLE move (
	id integer NOT NULL, 
	name varchar(50) NOT NULL,
	move_type varchar(10) NOT NULL,
	move_category varchar(10) NOT NULL,
	contest_category varchar(10),
	move_PP integer NOT NULL,
	move_power integer,
	move_accuracy integer,
	-- tags varchar(200),
	PRIMARY KEY (id)
);

CREATE TABLE ability (
	id integer NOT NULL,
	name varchar(50) NOT NULL,
	description varchar(200),
	PRIMARY KEY (id)
);

